package beans;

/**
 *
 * @author waxxan
 */
public class Intermediate {
    private String collageName;
    private String board;
    private String grade;
    private String totalMarks;
    private String uniRollNum;
    private String passingYear;
    private String district;
    private String obtainedMarks;

    /**
     * @return the collageName
     */
    public String getCollageName() {
        return collageName;
    }

    /**
     * @param collageName the collageName to set
     */
    public void setCollageName(String collageName) {
        this.collageName = collageName;
    }

    /**
     * @return the board
     */
    public String getBoard() {
        return board;
    }

    /**
     * @param board the board to set
     */
    public void setBoard(String board) {
        this.board = board;
    }

    /**
     * @return the grade
     */
    public String getGrade() {
        return grade;
    }

    /**
     * @param grade the grade to set
     */
    public void setGrade(String grade) {
        this.grade = grade;
    }

    /**
     * @return the totalMarks
     */
    public String getTotalMarks() {
        return totalMarks;
    }

    /**
     * @param totalMarks the totalMarks to set
     */
    public void setTotalMarks(String totalMarks) {
        this.totalMarks = totalMarks;
    }

    /**
     * @return the uniRollNum
     */
    public String getUniRollNum() {
        return uniRollNum;
    }

    /**
     * @param uniRollNum the uniRollNum to set
     */
    public void setUniRollNum(String uniRollNum) {
        this.uniRollNum = uniRollNum;
    }

    /**
     * @return the passingYear
     */
    public String getPassingYear() {
        return passingYear;
    }

    /**
     * @param passingYear the passingYear to set
     */
    public void setPassingYear(String passingYear) {
        this.passingYear = passingYear;
    }

    /**
     * @return the district
     */
    public String getDistrict() {
        return district;
    }

    /**
     * @param district the district to set
     */
    public void setDistrict(String district) {
        this.district = district;
    }

    /**
     * @return the obtainedMarks
     */
    public String getObtainedMarks() {
        return obtainedMarks;
    }

    /**
     * @param obtainedMarks the obtainedMarks to set
     */
    public void setObtainedMarks(String obtainedMarks) {
        this.obtainedMarks = obtainedMarks;
    }
}
